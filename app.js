function buildCandles(ticker) {
  return ticker.history.slice(-28).map((close, index, list) => {
    const open = index === 0 ? list[index] * (1 - ticker.volatility * 1.5) : list[index - 1];
    const spread = Math.max(Math.abs(close - open), ticker.price * ticker.volatility * (1.8 + (index % 5) * 0.18));
    const high = Math.max(open, close) + spread * 0.55;
    const low = Math.min(open, close) - spread * 0.55;
    const volume = 42 + Math.abs(close - open) / Math.max(0.0001, ticker.price * ticker.volatility) * 18 + ((index * 13) % 24);
    return { open, high, low, close, volume };
  });
}

const timeframeMinutes = {
  "1m": 1,
  "5m": 5,
  "15m": 15,
  "1h": 60,
  "1D": 1440
};

function hashSymbol(symbol) {
  return symbol.split("").reduce((sum, char) => sum + char.charCodeAt(0), 0);
}

function generateBars(ticker, timeframe = selectedTimeframe) {
  const minutes = timeframeMinutes[timeframe] || 15;
  const barCount = timeframe === "1D" ? 90 : 130;
  const now = Math.floor(Date.now() / 1000);
  const seed = hashSymbol(ticker.symbol);
  const data = [];
  let close = ticker.price * (1 - ticker.change / 100);

  for (let index = 0; index < barCount; index += 1) {
    const wave = Math.sin((index + seed) / 8) * ticker.volatility * 3.8;
    const pulse = Math.cos((index + seed) / 17) * ticker.volatility * 2.6;
    const drift = ticker.change / 100 / barCount;
    const open = close;
    close = Math.max(0.0001, close * (1 + drift + wave * 0.24 + pulse * 0.16));
    const spread = Math.max(Math.abs(close - open), ticker.price * ticker.volatility * (2 + (index % 7) * 0.22));
    const high = Math.max(open, close) + spread * (0.5 + (index % 3) * 0.08);
    const low = Math.min(open, close) - spread * (0.5 + ((index + 1) % 3) * 0.08);
    const time = now - (barCount - index) * minutes * 60;
    const volume = Math.round(800 + Math.abs(close - open) / Math.max(0.0001, ticker.price * ticker.volatility) * 280 + ((index * seed) % 900));
    data.push({ time, open, high, low, close, volume });
  }

  const last = data[data.length - 1];
  last.close = ticker.price;
  last.high = Math.max(last.high, ticker.price);
  last.low = Math.min(last.low, ticker.price);
  return data;
}

async function loadChartBars(ticker, timeframe = selectedTimeframe) {
  const isStaticPages = window.location.hostname.endsWith("github.io") || !["localhost", "127.0.0.1"].includes(window.location.hostname);
  if (isStaticPages) {
    activeChartProvider = "Static demo";
    return generateBars(ticker, timeframe);
  }

  try {
    const response = await fetch(`/api/yahoo-chart?symbol=${encodeURIComponent(ticker.symbol)}&timeframe=${encodeURIComponent(timeframe)}`);
    if (!response.ok) throw new Error(`Yahoo proxy ${response.status}`);
    const payload = await response.json();
    if (!Array.isArray(payload.bars) || payload.bars.length < 10) throw new Error("Yahoo payload has no bars");
    activeChartProvider = `${payload.provider} · ${payload.provider_symbol}`;
    return payload.bars;
  } catch (error) {
    activeChartProvider = "Mock simulation";
    return generateBars(ticker, timeframe);
  }
}

function renderTradingChart(ticker) {
  const candles = buildCandles(ticker);
  const chartWidth = 920;
  const chartHeight = 330;
  const volumeTop = 245;
  const volumeHeight = 66;
  const priceHigh = Math.max(...candles.map((candle) => candle.high));
  const priceLow = Math.min(...candles.map((candle) => candle.low));
  const priceRange = priceHigh - priceLow || 1;
  const maxVolume = Math.max(...candles.map((candle) => candle.volume));
  const step = chartWidth / candles.length;
  const bodyWidth = Math.max(8, step * 0.58);
  const yFor = (price) => 18 + ((priceHigh - price) / priceRange) * 214;
  const priceLines = Array.from({ length: 5 }, (_, index) => {
    const y = 18 + index * 53.5;
    const price = priceHigh - (index / 4) * priceRange;
    return { y, price };
  });
  const candleMarkup = candles.map((candle, index) => {
    const x = index * step + step / 2;
    const openY = yFor(candle.open);
    const closeY = yFor(candle.close);
    const highY = yFor(candle.high);
    const lowY = yFor(candle.low);
    const up = candle.close >= candle.open;
    const color = up ? "#2ed889" : "#ff5c70";
    const bodyY = Math.min(openY, closeY);
    const bodyHeight = Math.max(2, Math.abs(closeY - openY));
    const volumeHeightValue = Math.max(3, (candle.volume / maxVolume) * volumeHeight);
    return `
      <line x1="${x.toFixed(1)}" y1="${highY.toFixed(1)}" x2="${x.toFixed(1)}" y2="${lowY.toFixed(1)}" stroke="${color}" stroke-width="1.5"></line>
      <rect x="${(x - bodyWidth / 2).toFixed(1)}" y="${bodyY.toFixed(1)}" width="${bodyWidth.toFixed(1)}" height="${bodyHeight.toFixed(1)}" rx="1.5" fill="${up ? color : "transparent"}" stroke="${color}" stroke-width="1.8"></rect>
      <rect x="${(x - bodyWidth / 2).toFixed(1)}" y="${(volumeTop + volumeHeight - volumeHeightValue).toFixed(1)}" width="${bodyWidth.toFixed(1)}" height="${volumeHeightValue.toFixed(1)}" fill="${color}" opacity="0.32"></rect>
    `;
  }).join("");
  const currentY = yFor(ticker.price);

  return `
    <svg class="trading-chart" viewBox="0 0 1000 330" preserveAspectRatio="none" role="img" aria-label="${ticker.symbol} ${t("chart")}">
      <rect x="0" y="0" width="1000" height="330" fill="#0b1311"></rect>
      ${Array.from({ length: 9 }, (_, index) => `<line x1="${index * 115}" y1="0" x2="${index * 115}" y2="330" stroke="#20322d" stroke-width="1"></line>`).join("")}
      ${priceLines.map((line) => `<line x1="0" y1="${line.y.toFixed(1)}" x2="920" y2="${line.y.toFixed(1)}" stroke="#20322d" stroke-width="1"></line>`).join("")}
      <line x1="0" y1="${volumeTop}" x2="920" y2="${volumeTop}" stroke="#31463f" stroke-width="1"></line>
      ${candleMarkup}
      <line x1="0" y1="${currentY.toFixed(1)}" x2="920" y2="${currentY.toFixed(1)}" stroke="${ticker.change >= 0 ? "#2ed889" : "#ff5c70"}" stroke-width="1.2" stroke-dasharray="5 5"></line>
      <rect x="926" y="${Math.max(6, currentY - 12).toFixed(1)}" width="68" height="24" rx="4" fill="${ticker.change >= 0 ? "#173225" : "#3a1820"}" stroke="${ticker.change >= 0 ? "#2ed889" : "#ff5c70"}"></rect>
      <text x="960" y="${Math.max(22, currentY + 4).toFixed(1)}" fill="${ticker.change >= 0 ? "#2ed889" : "#ff5c70"}" font-size="12" text-anchor="middle">${formatPrice(ticker)}</text>
      ${priceLines.map((line) => `<text x="992" y="${(line.y + 4).toFixed(1)}" fill="#8fa09a" font-size="11" text-anchor="end">${line.price.toFixed(ticker.decimals)}</text>`).join("")}
      <text x="8" y="305" fill="#8fa09a" font-size="11">${t("volume")}</text>
    </svg>
  `;
}

function buildPressureState(candles) {
  const recent = candles.slice(-40);
  const maxVolume = Math.max(...recent.map((bar) => bar.volume || 1));
  const buyPressure = recent.reduce((sum, bar) => {
    const body = Math.max(0, bar.close - bar.open);
    const range = Math.max(0.0001, bar.high - bar.low);
    return sum + (body / range) * ((bar.volume || 1) / maxVolume);
  }, 0);
  const sellPressure = recent.reduce((sum, bar) => {
    const body = Math.max(0, bar.open - bar.close);
    const range = Math.max(0.0001, bar.high - bar.low);
    return sum + (body / range) * ((bar.volume || 1) / maxVolume);
  }, 0);
  const total = buyPressure + sellPressure || 1;
  const buyerStrength = Math.round((buyPressure / total) * 100);
  const sellerStrength = 100 - buyerStrength;
  const imbalance = buyerStrength - sellerStrength;
  const biasClass = imbalance >= 8 ? "positive" : imbalance <= -8 ? "negative" : "";
  const biasLabel = currentLanguage === "tr"
    ? imbalance >= 8 ? "Alıcı baskın" : imbalance <= -8 ? "Satıcı baskın" : "Dengeli"
    : imbalance >= 8 ? "Buyers leading" : imbalance <= -8 ? "Sellers leading" : "Balanced";
  const flowText = currentLanguage === "tr"
    ? imbalance >= 8
      ? "Alıcı tarafı daha kalın. Dalga yukarı eğimli kalırsa geri çekilmeler alım fırsatı gibi okunabilir."
      : imbalance <= -8
        ? "Satıcı tarafı daha ağır. Dalga aşağı eğimli kaldıkça yükselişler satışla karşılanıyor."
        : "Akış dengeli. Net yön için haber veya hacim teyidi beklemek daha doğru."
    : imbalance >= 8
      ? "The buy side is thicker. If the wave keeps sloping up, pullbacks read like buyable dips."
      : imbalance <= -8
        ? "The sell side is heavier. While the wave slopes down, rallies are being sold."
        : "Flow is balanced. Wait for news or volume confirmation before reading direction.";

  return { buyerStrength, sellerStrength, imbalance, biasClass, biasLabel, flowText };
}

