function updateLivePrices() {
  tickers.forEach((ticker) => {
    const drift = (Math.random() - 0.48) * ticker.volatility;
    ticker.price = Math.max(0.0001, ticker.price * (1 + drift));
    ticker.change = ((ticker.price - ticker.base) / ticker.base) * 100;
    ticker.history = [...ticker.history.slice(1), ticker.price];
  });

  currencyPairs.forEach((pair) => {
    const drift = (Math.random() - 0.5) * 0.0009;
    pair.price = Math.max(0.0001, pair.price * (1 + drift));
    pair.change += (Math.random() - 0.5) * 0.03;
  });

  updateTickerDom();
  updateFavoritesDom();
  renderWatchlist();
  renderCurrencyPairs();
  if (document.getElementById("chartDock").classList.contains("open")) {
    updateInteractiveChartData();
  }
}

function bindControls() {
  ["assetClassFilter", "windowFilter", "confidenceFilter", "watchlistOnly"].forEach((id) => {
    document.getElementById(id).addEventListener("change", renderMovements);
  });

  document.getElementById("languageSelect").addEventListener("change", (event) => {
    currentLanguage = event.target.value;
    document.documentElement.lang = currentLanguage;
    updateStaticText();
    renderTickers();
    if (document.getElementById("chartDock").classList.contains("open")) {
      renderChartDock();
    }
    renderMovements();
    renderNews();
    renderFavorites();
    renderWatchlist();
    renderCurrencyPairs();
    renderEventCalendar();
  });

  document.getElementById("resetFilters").addEventListener("click", () => {
    document.getElementById("assetClassFilter").value = "all";
    document.getElementById("windowFilter").value = "all";
    document.getElementById("confidenceFilter").value = "all";
    document.getElementById("watchlistOnly").checked = false;
    renderMovements();
  });

  document.querySelectorAll(".segment").forEach((button) => {
    button.addEventListener("click", () => {
      document.querySelectorAll(".segment").forEach((item) => item.classList.remove("active"));
      button.classList.add("active");
      sortMode = button.dataset.sort;
      renderMovements();
    });
  });

  document.querySelectorAll(".calendar-tab").forEach((button) => {
    button.addEventListener("click", () => {
      document.querySelectorAll(".calendar-tab").forEach((item) => item.classList.remove("active"));
      button.classList.add("active");
      activeCalendarRange = button.dataset.calendarRange;
      renderEventCalendar();
    });
  });

  document.addEventListener("click", (event) => {
    const popover = document.getElementById("newsPopover");
    if (!popover.classList.contains("open")) return;
    if (popover.contains(event.target) || event.target.closest(".news-item")) return;
    closeNewsPopover();
  });

  window.addEventListener("resize", closeNewsPopover);
  window.addEventListener("scroll", closeNewsPopover, true);
}

