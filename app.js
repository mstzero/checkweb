function renderNews() {
  const list = document.getElementById("newsList");
  list.innerHTML = news.map((item) => `
    <button class="news-item" data-news-id="${item.id}" title="${t("readDetails")}">
      <strong>${localText(item.title)}</strong>
      <div class="meta-line">
        <span>${item.source}</span>
        <span>${item.asset}</span>
        <span>${item.sentiment}</span>
        <span>${t("impactLabel")} ${item.impact}/10</span>
      </div>
    </button>
  `).join("");

  document.querySelectorAll(".news-item").forEach((button) => {
    button.addEventListener("click", (event) => {
      event.stopPropagation();
      const item = news.find((entry) => entry.id === button.dataset.newsId);
      if (item) renderNewsPopover(item, button);
    });
  });
}

function renderNewsPopover(item, anchor) {
  const popover = document.getElementById("newsPopover");
  const rect = anchor.getBoundingClientRect();
  const meta = `${item.source} · ${item.asset} · ${item.sentiment} · ${t("impactLabel")} ${item.impact}/10`;

  popover.innerHTML = `
    <div class="popover-title">
      <div>
        <span class="label">${t("newsDetail")}</span>
        <h3>${localText(item.title)}</h3>
        <div class="meta-line">
          <span>${meta}</span>
          <span>${t("sourceReliability")}: ${item.reliability}</span>
        </div>
      </div>
      <button class="popover-close" id="closeNewsPopover" title="${t("close")}">X</button>
    </div>
    <div class="popover-section">
      <h4>${t("summary")}</h4>
      <p>${localText(item.summary)}</p>
    </div>
    <div class="popover-section">
      <h4>${t("whyMatters")}</h4>
      <p>${localText(item.why)}</p>
    </div>
    <div class="popover-section">
      <h4>${t("marketImpact")}</h4>
      <p>${localText(item.impactText)}</p>
    </div>
    <div class="popover-section">
      <h4>${t("suggestedAction")}</h4>
      <p>${localText(item.note)}</p>
    </div>
    <div class="popover-section">
      <h4>${t("relatedAssets")}</h4>
      <div class="asset-pills">${item.assets.map((asset) => `<span class="asset-pill">${asset}</span>`).join("")}</div>
    </div>
  `;

  const width = Math.min(430, window.innerWidth - 28);
  const left = Math.max(14, Math.min(window.innerWidth - width - 14, rect.left - width - 12));
  const preferredTop = rect.top;
  const maxTop = window.innerHeight - Math.min(620, window.innerHeight - 28) - 14;
  const top = Math.max(14, Math.min(maxTop, preferredTop));

  popover.style.left = `${left}px`;
  popover.style.top = `${top}px`;
  popover.classList.add("open");

  let arrow = document.getElementById("newsPopoverArrow");
  if (!arrow) {
    arrow = document.createElement("div");
    arrow.id = "newsPopoverArrow";
    arrow.className = "popover-arrow";
    document.body.appendChild(arrow);
  }
  arrow.style.left = `${Math.max(20, left + width - 7)}px`;
  arrow.style.top = `${Math.max(20, rect.top + 18)}px`;
  arrow.classList.add("open");

  document.getElementById("closeNewsPopover").addEventListener("click", closeNewsPopover);
}

function closeNewsPopover() {
  document.getElementById("newsPopover").classList.remove("open");
  const arrow = document.getElementById("newsPopoverArrow");
  if (arrow) arrow.classList.remove("open");
}

